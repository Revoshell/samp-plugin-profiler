#include "function_statistics.h"

namespace amxprof {

FunctionStatistics::FunctionStatistics(Function *fn)
 : fn_(fn),
   num_calls_(0)
{
}

void FunctionStatistics::AdjustSelfTime(Nanoseconds delta) {
  self_time_ += delta;
}

void FunctionStatistics::AdjustTotalTime(Nanoseconds delta) {
  total_time_ += delta;
}

} // namespace amxprof
